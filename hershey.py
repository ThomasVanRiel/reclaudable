_UNKNOWN_ADVANCE = 0.55   # advance for an unmapped char (rendered blank)


def measure(text: str, size: float, spacing: float | None = None) -> float:
    """Total advance width of `text` at cap-height `size` (in the same units as
    the strokes returned by `text_to_polylines`)."""
    if spacing is None:
        spacing = 0.15 * size
    w = 0.0
    for ch in text.upper():
        adv, _ = GLYPHS.get(ch, (_UNKNOWN_ADVANCE, []))
        w += adv * size + spacing
    return max(w - spacing, 0.0) if text else 0.0


def text_to_polylines(text: str, x: float, y: float, size: float,
                      spacing: float | None = None) -> list[list[tuple[float, float]]]:
    """Render `text` as single-stroke glyph polylines, top-left anchored at
    (x, y), cap height `size`. Coords match the caller's space (x right, y down).
    Lowercase is folded to uppercase. Unmapped chars advance but draw nothing."""
    if spacing is None:
        spacing = 0.15 * size
    out: list[list[tuple[float, float]]] = []
    cx = x
    for ch in text.upper():
        adv, strokes = GLYPHS.get(ch, (_UNKNOWN_ADVANCE, []))
        for stroke in strokes:
            out.append([(cx + px * size, y + py * size) for px, py in stroke])
        cx += adv * size + spacing
    return out
